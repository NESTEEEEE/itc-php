				<P align="justify"> <b> *<u> Version Control/Git- </b> </u>Version control software—like open source stalwart Git—is a tool you’ll use to track those changes so you can go back to a previous version of your work and find out what went wrong without tearing the whole thing down.

As you might imagine, this a skill that you (and your prospective clients and employers) will be very happy to have.	</P> 
<br>
<br>				
				<P align="justify"> <b> *<u> Responsive Design- </b> </u>These days we use any number of computers, phones, and tablets to look at web pages. Ever notice how these pages adjust themselves to the device you’re using without you doing anything on your end? This is due to responsive design. Understanding responsive design principles and how to implement them on the coding side is key to front end development.

One cool thing to keep in mind regarding responsive design is that it’s an intrinsic part of CSS frameworks like the aforementioned Bootstrap. These skills are all interconnected and so as you learn one you’ll often be making progress in the others at the same time.	</P> 
<br>
<br>
				<P align="justify"> <b> *<u> Testing/Debugging- </b> </u> There are a couple of different testing methods for web development. Functional testing looks at a particular piece of functionality on your site (like a form or database) and makes sure it does everything you’ve coded it to do.

Unit testing is another method. It tests the smallest bit of code that’s responsible for one thing on your site and examines it individually for correct operation.

Testing is a big part of the front end development process, but fortunately, it’s another area where there are frameworks to help you. Programs like Mocha and Jasmine are designed to speed up and simplify your testing process.  </P>
<br>
<br>
				<P align="justify"> <b> *<u> Browser Developer Tools- </b> </u>Specifics will vary from browser to browser, but browser developer tools generally consist of an inspector and a JavaScript console. The inspector allows you to see what the runtime HTML on your page looks like, what CSS is associated with each element on the page, and also allows you to edit your HTML and CSS and see the changes live as they happen. The JS console allows you to view any errors that occur as the browser tries to execute your JS code.	</P> 
<br>
<br>
				<P align="justify"> <b> *<u> Building and Automation Tools/Web Performance- </b> </u>	You might be seeing a pattern that HTML, CSS, and JavaScript are the three primary tools for front end development. Meanwhile, most of the other skills either help make those tools more efficient or help test your site and fix mistakes. The trend continues here with building and automation tools and web performance.</P> 
