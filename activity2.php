<br>				
				<P align="justify"> <b> *<u> HTML or CSS- </b> </u> You need to know HTML, because its the most basic building block you'll need for developing website.
								Also you must have skill in CSS to create the pages layout, color, fonts, and the color. </P> 
<br>
<br>				
				<P align="justify"> <b> *<u> JavaScript/Jquery- </b> </u> Another MAJOR tool in your front end developer toolbox is going to be JavaScript (JS). Where HTML is a markup language and CSS is a style sheet language, JS is the first language I’ve mentioned that’s a bonafide programming language. What’s the difference? Where HTML and CSS determine the presentation of a page, JS determines the function.

In some instances a very simple website or web page is fine, but for situations where you need interactive features—audio and video, games, scrolling abilities, page animations—JS is the tool you’ll use to implement them (though as CSS evolves, it’s starting to handle a lot of these duties as well).

One cool thing to keep in mind about JS is the existence of libraries like jQuery, a collection of plugins and extensions that make it faster and easier to use JS on your website. jQuery takes common tasks that require multiple lines of JS code and compresses them into a format that can be executed with a single line. This will be a big help when you’re coding with JS. Unless, of course, you don’t like saving time.  </P> 
<br>				
<br>
				<P align="justify"> <b> *<u> CSS and JavaScript Frameworks- </b> </u>	CSS and JavaScript frameworks are collections of CSS or JS files that do a bunch of the work for you by providing common functionality (think logging into a website or searching a blog). Instead of starting with an EMPTY text document you start with a code file that has lots of awesome JS already in it.

Frameworks have their strengths and weaknesses—don’t we all!—and it’s important to choose the best framework for the type of website you’re building. For example, some JS frameworks are great for building complex user interfaces, while others excel at displaying all of your site’s content.

To make things even MORE fun you can use frameworks together. It’s common to pair Bootstrap with another JavaScript framework like AngularJS. The content is handled by Angular, and the look & feel is handled by Bootstrap (with some CSS sprinkled in, too).

Since you’ll be using CSS and JS all the time in your web development, and since many projects start with similar style elements and code, knowledge of these frameworks is critical to being an efficient developer.	</P> 
<br>
<br>
				<P align="justify"> <b> *<u> CSS Preprocessing- </b> </u>	CSS on its own, while essential, can sometimes be limiting. One of these limitations is that fact that you can’t define variables, functions, or perform arithmetic operations. This is a problem when a project grows in scale and code base, as you’ll soon find yourself wasting a lot of time writing repetitive code to make changes. Like CSS (and JS) frameworks, CSS preprocessing is another means of making your life as a developer easier and more flexible.

Using a CSS preprocessor like Sass, LESS, or Stylus, you’re able to write code in the preprocessor’s language (allowing you to do things that might be a huge pain with plain old CSS). The preprocessor then converts that code to CSS so it will work on your website.

Let’s say you decide to tweak the shade of blue you’re using across a site. With a CSS preprocessor, you’d only have to change the hex value in one place instead of going through ALLLL your CSS and changing the hex values everywhere.	</P> 

